package day06junit.junit;

public class Calculator {

    /**
     * 加法方法。
     * @param a
     * @param b
     * @return
     */
    public int add(int a, int b) {
        return a+b;
    }

    /**
     * 减法方法。
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b) {
        return a-b;
    }

}
